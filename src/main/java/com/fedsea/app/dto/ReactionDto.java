package com.fedsea.app.dto;

public class ReactionDto {
	private int like;
	private int angel;
	private int love;
	private int hahaha;

	public int getLike() {
		return like;
	}

	public void setLike(int like) {
		this.like = like;
	}

	public int getAngel() {
		return angel;
	}

	public void setAngel(int angel) {
		this.angel = angel;
	}

	public int getLove() {
		return love;
	}

	public void setLove(int love) {
		this.love = love;
	}

	public int getHahaha() {
		return hahaha;
	}

	public void setHahaha(int hahaha) {
		this.hahaha = hahaha;
	}

}
